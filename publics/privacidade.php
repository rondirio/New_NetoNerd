            <ul>
                <li>Diretamente pelo Usuário, ao preencher formulários de cadastro, contato ou abertura de chamados;</li>
                <li>Automaticamente, por meio de cookies e logs de servidor durante o uso da Plataforma;</li>
                <li>Por meio de terceiros, quando contratamos serviços de análise ou comunicação (ex.: Google Analytics, servidores de e-mail).</li>
            </ul>

