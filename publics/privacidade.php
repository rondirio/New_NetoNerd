                <li>Comunicação sobre atualizações, manutenções e novidades dos serviços;</li>
                <li>Faturamento e controle financeiro;</li>
                <li>Cumprimento de obrigações legais e regulatórias;</li>
                <li>Melhoria contínua da Plataforma por meio de análise de uso;</li>
                <li>Prevenção de fraudes e garantia da segurança da Plataforma.</li>
            </ul>

            <h2>4. Base Legal para o Tratamento</h2>
            <p>O tratamento de dados pela NetoNerd está fundamentado nas seguintes bases legais da LGPD:</p>
            <ul>
                <li><strong>Execução de contrato</strong> (art. 7º, V): quando o tratamento é necessário para prestação do serviço contratado;</li>
                <li><strong>Legítimo interesse</strong> (art. 7º, IX): para fins de segurança, prevenção de fraudes e melhoria dos serviços;</li>
                <li><strong>Cumprimento de obrigação legal</strong> (art. 7º, II): quando exigido por lei ou autoridade competente;</li>
                <li><strong>Consentimento</strong> (art. 7º, I): para envio de comunicações de marketing, quando aplicável.</li>
            </ul>

            <h2>5. Compartilhamento de Dados</h2>
            <p>
                A NetoNerd não vende, aluga ou cede seus dados pessoais a terceiros. O compartilhamento ocorre apenas
                nas seguintes situações:
            </p>
            <ul>
                <li><strong>Prestadores de serviço:</strong> parceiros técnicos que auxiliam na operação da Plataforma (hospedagem, e-mail transacional), sempre sob obrigação de confidencialidade;</li>
                <li><strong>Cumprimento legal:</strong> quando exigido por autoridade judicial, regulatória ou governamental competente;</li>
                <li><strong>Proteção de direitos:</strong> quando necessário para exercer ou defender direitos da NetoNerd.</li>
            </ul>

            <h2>6. Cookies e Tecnologias de Rastreamento</h2>
            <p>
                Utilizamos cookies essenciais para o funcionamento da Plataforma (sessão do usuário, segurança CSRF)
                e cookies analíticos para entender o comportamento de uso. Você pode configurar seu navegador para
                bloquear cookies, mas isso pode afetar o funcionamento de algumas funcionalidades.
            </p>
            <ul>
                <li><strong>Cookies de sessão:</strong> necessários para manter o login ativo durante o uso;</li>
                <li><strong>Cookies analíticos:</strong> coletam dados agregados e anônimos sobre o uso da Plataforma.</li>
            </ul>

            <h2>7. Armazenamento e Segurança</h2>
            <p>
                Seus dados são armazenados em servidores seguros localizados no Brasil, operados pela Hostinger.
                Adotamos medidas técnicas e organizacionais para proteger seus dados contra acesso não autorizado,
                perda, destruição ou divulgação indevida, incluindo:
            </p>
            <ul>
                <li>Criptografia de senhas com bcrypt;</li>
                <li>Comunicações protegidas por SSL/TLS;</li>
                <li>Controle de acesso por perfil de usuário;</li>
                <li>Logs de auditoria de acesso ao sistema.</li>
            </ul>

            <h2>8. Retenção de Dados</h2>
            <p>
                Mantemos seus dados pelo tempo necessário para cumprir as finalidades descritas nesta Política ou
                pelo prazo exigido por lei. Dados de clientes ativos são retidos durante toda a vigência do contrato.
