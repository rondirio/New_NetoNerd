                <li><strong>Dados de pagamento:</strong> informações necessárias para faturamento (não armazenamos dados de cartão de crédito diretamente).</li>
                <li><strong>Dados de comunicação:</strong> mensagens enviadas via formulário de contato ou e-mail.</li>
            </ul>

