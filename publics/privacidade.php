            <p>De acordo com o serviço utilizado, podemos coletar as seguintes categorias de dados:</p>
            <ul>
                <li><strong>Dados de identificação:</strong> nome completo, CPF, e-mail, telefone e endereço.</li>
                <li><strong>Dados de acesso:</strong> endereço IP, data e hora de login, tipo de dispositivo e navegador.</li>
